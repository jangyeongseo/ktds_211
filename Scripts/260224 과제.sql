-- 5. 부서명이 가장 긴 부서에서 근무중인 사원의 모든 정보를 조회한다. - 0ROW
SELECT
	E.EMPLOYEE_ID,
	E.FIRST_NAME,
	E.LAST_NAME,
	E.EMAIL,
	E.PHONE_NUMBER,
	E.HIRE_DATE,
	E.JOB_ID,
	E.SALARY,
	E.COMMISSION_PCT,
	E.MANAGER_ID
FROM
	EMPLOYEES E
WHERE
	E.DEPARTMENT_ID = (
	SELECT
		DEPARTMENT_ID
	FROM
		DEPARTMENTS
	ORDER BY
		LENGTH(DEPARTMENT_NAME) DESC
        FETCH FIRST 1 ROWS ONLY
    );

-- 6. 2002년부터 2006년까지 입사한 사원은 몇 명인지 연도별로 조회한다. - 5ROW
SELECT
	TO_CHAR(HIRE_DATE, 'YYYY') AS HIRE_YEAR,
	COUNT(*) AS EMP_COUNT
FROM
	EMPLOYEES
WHERE
	TO_CHAR(HIRE_DATE, 'YYYY') BETWEEN '2002' AND '2006'
GROUP BY
	TO_CHAR(HIRE_DATE, 'YYYY')
ORDER BY
	HIRE_YEAR
;

SELECT
	HIRE_YEAR,
	EMP_COUNT
FROM
	(
	SELECT
		TO_CHAR(HIRE_DATE, 'YYYY') AS HIRE_YEAR,
		COUNT(*) AS EMP_COUNT
	FROM
		EMPLOYEES
	WHERE
		TO_CHAR(HIRE_DATE, 'YYYY') BETWEEN '2002' AND '2006'
	GROUP BY
		TO_CHAR(HIRE_DATE, 'YYYY')
)
ORDER BY
	HIRE_YEAR;


-- 7. 입사일이 가장 빠른 사원 5명의 이름과 입사일을 조회한다. - 5ROW
SELECT
	R_NUM,
	HIRE_DATE,
	FIRST_NAME
FROM
	(
	SELECT
		ROWNUM AS R_NUM,
		HIRE_DATE,
		FIRST_NAME
	FROM
		(
		SELECT
			HIRE_DATE,
			FIRST_NAME
		FROM
			EMPLOYEES
		ORDER BY
			HIRE_DATE ASC
            )
    )
WHERE
	R_NUM <= 5
;

-- 8. 커미션을 받는 사원들의 이름과 커미션을 조회한다. 단, 커미션이 가장 높은 사원 3명은 제외한다. - 31ROW
SELECT
	FIRST_NAME,
	COMMISSION_PCT
FROM
	(
	SELECT
		FIRST_NAME,
		COMMISSION_PCT,
		RANK() OVER (
		ORDER BY 
		COMMISSION_PCT DESC
		) AS RNK
	FROM
		EMPLOYEES
	WHERE
		COMMISSION_PCT IS NOT NULL
    )
WHERE
	RNK > 3
;

