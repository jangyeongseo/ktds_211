    LEVEL,
    EMPLOYEE_ID,
    FIRST_NAME,
    MANAGER_ID,
    DEPARTMENT_ID
FROM
    EMPLOYEES
START WITH
    EMPLOYEE_ID = (
        SELECT MANAGER_ID
        FROM DEPARTMENTS
        WHERE DEPARTMENT_NAME = 'IT'
    )
