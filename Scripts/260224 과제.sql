-- 1. 100번 사원의 모든 부하직원을 계층조회한다. - 107ROW
SELECT
	LEVEL,
	EMPLOYEE_ID,
	FIRST_NAME,
	MANAGER_ID
FROM
	EMPLOYEES
START WITH
	EMPLOYEE_ID = 100
CONNECT BY
	PRIOR EMPLOYEE_ID = MANAGER_ID
;

