-- 4. 부서장들의 부하직원을 계층조회한다. - 147ROW
SELECT
	LEVEL,
	DEPARTMENT_ID,
	EMPLOYEE_ID,
	FIRST_NAME,
	MANAGER_ID
FROM
	EMPLOYEES
START WITH
	EMPLOYEE_ID IN (
	SELECT
		MANAGER_ID
	FROM
		DEPARTMENTS
	WHERE
		MANAGER_ID IS NOT NULL
    )
CONNECT BY
	PRIOR EMPLOYEE_ID = MANAGER_ID
ORDER SIBLINGS BY DEPARTMENT_ID ASC
	-- 부서장들의 부서정보로 정렬해라
;

-- 5. 부서명이 가장 긴 부서에서 근무중인 사원의 모든 정보를 조회한다. - 0ROW
SELECT
	E.EMPLOYEE_ID,
	E.FIRST_NAME,
	E.LAST_NAME,
	E.EMAIL,
	E.PHONE_NUMBER,
	E.HIRE_DATE,
	E.JOB_ID,
	E.SALARY,
	E.COMMISSION_PCT,
	E.MANAGER_ID
FROM
	EMPLOYEES E
WHERE
	E.DEPARTMENT_ID = (
	SELECT
		DEPARTMENT_ID
	FROM
		DEPARTMENTS
	ORDER BY
		LENGTH(DEPARTMENT_NAME) DESC
        FETCH FIRST 1 ROWS ONLY
    );

SELECT
	*
FROM
	EMPLOYEES
WHERE
	DEPARTMENT_ID = (
	SELECT
		DEPARTMENT_ID
	FROM
		(
		SELECT
			DEPARTMENT_ID,
			LENGTH(DEPARTMENT_NAME) AS DEP_NAME
		FROM
			DEPARTMENTS
		ORDER BY
			DEP_NAME DESC
		)
	WHERE
		ROWNUM = 1)
;

-- 6. 2002년부터 2006년까지 입사한 사원은 몇 명인지 연도별로 조회한다. - 5ROW
SELECT
	TO_CHAR(HIRE_DATE, 'YYYY') AS HIRE_YEAR,
	COUNT(EMPLOYEE_ID) AS EMP_COUNT
FROM
	EMPLOYEES
WHERE
	TO_CHAR(HIRE_DATE, 'YYYY') BETWEEN '2002' AND '2006'
GROUP BY
	TO_CHAR(HIRE_DATE, 'YYYY')
ORDER BY
	HIRE_YEAR
;

SELECT
	HIRE_YEAR,
	EMP_COUNT
FROM
	(
	SELECT
		TO_CHAR(HIRE_DATE, 'YYYY') AS HIRE_YEAR,
		COUNT(EMPLOYEE_ID) AS EMP_COUNT
	FROM
		EMPLOYEES
	WHERE
		TO_CHAR(HIRE_DATE, 'YYYY') BETWEEN '2002' AND '2006'
	GROUP BY
		TO_CHAR(HIRE_DATE, 'YYYY')
)
ORDER BY
	HIRE_YEAR
;

SELECT
	HIRE_YEAR,
	COUNT(EMPLOYEE_ID)
FROM
	(
	SELECT
		TO_CHAR(HIRE_DATE, 'YYYY') AS HIRE_YEAR,
		EMPLOYEE_ID
	FROM
		EMPLOYEES
	WHERE
		HIRE_DATE BETWEEN TO_DATE('2002-01-01', 'YYYY-MM-DD') AND TO_DATE('2007-01-01', 'YYYY-MM-DD') -1)
GROUP BY HIRE_YEAR
ORDER BY HIRE_YEAR
;

-- 7. 입사일이 가장 빠른 사원 5명의 이름과 입사일을 조회한다. - 5ROW
SELECT
	HIRE_DATE,
	FIRST_NAME
FROM
	(
	SELECT
		HIRE_DATE,
		FIRST_NAME
	FROM
		(
		SELECT
			HIRE_DATE,
			FIRST_NAME
		FROM
			EMPLOYEES
		ORDER BY
			HIRE_DATE ASC))
WHERE
	ROWNUM <= 5
;

-- 8. 커미션을 받는 사원들의 이름과 커미션을 조회한다. 단, 커미션이 가장 높은 사원 3명은 제외한다. - 31ROW
SELECT
	FIRST_NAME,
	COMMISSION_PCT
FROM
	EMPLOYEES
WHERE
	COMMISSION_PCT IS NOT NULL
	AND COMMISSION_PCT NOT IN(
	SELECT
		COMMISSION_PCT
	FROM
		( SELECT
			DISTINCT COMMISSION_PCT
		FROM
			EMPLOYEES
		WHERE
			COMMISSION_PCT IS NOT NULL
		ORDER BY COMMISSION_PCT DESC)
	WHERE
		ROWNUM >= 3)
;

